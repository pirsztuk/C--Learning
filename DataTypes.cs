// Integer number

int integerValue = 1;


// Fractional number (from less precise to more precise)

// To set a float literal we use F
float floatValue = 1.25F; // 6-9 digits after a comma


// To create a double literal, just enter a decimal number.  No suffix is needed
double doubleValue = 1.25; // 15-17 digits after a comma


// To set a decimal literal we use M
decimal decimalValue = 1.25M; // 28-29 digits after a comma


// String and char

string stringValue = "A value with a text"; // Double quotes ESSENTIAL

char charValue = 'H'; // Single quotes ESSENTIAL and only one char inside


// Boolean type

bool booleanValue = true; // true/false


